package com.wanfin.fpd.modules.wish.contract.vo;

public class DeclareVo {

	private String money;//借款金额
	private String moneyCapital;//借款金额大写
	private String monthRate;//月利率%
	private String monthFee;//月利息（元）
	private String wishCharge;//手续费
	private String sumFee;//综合服务费
	private String realMoney;//实际到账金额
	private String orderMoney;//订单金额
	private String days;//融资期限
	
	
	
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getMoneyCapital() {
		return moneyCapital;
	}
	public void setMoneyCapital(String moneyCapital) {
		this.moneyCapital = moneyCapital;
	}
	public String getMonthRate() {
		return monthRate;
	}
	public void setMonthRate(String monthRate) {
		this.monthRate = monthRate;
	}
	public String getMonthFee() {
		return monthFee;
	}
	public void setMonthFee(String monthFee) {
		this.monthFee = monthFee;
	}
	public String getWishCharge() {
		return wishCharge;
	}
	public void setWishCharge(String wishCharge) {
		this.wishCharge = wishCharge;
	}
	public String getSumFee() {
		return sumFee;
	}
	public void setSumFee(String sumFee) {
		this.sumFee = sumFee;
	}
	public String getRealMoney() {
		return realMoney;
	}
	public void setRealMoney(String realMoney) {
		this.realMoney = realMoney;
	}
	public String getOrderMoney() {
		return orderMoney;
	}
	public void setOrderMoney(String orderMoney) {
		this.orderMoney = orderMoney;
	}
	public String getDays() {
		return days;
	}
	public void setDays(String days) {
		this.days = days;
	}
	
	
	
	
	
	
	
}
